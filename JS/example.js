// Run this file to see if JS is running || Hit on VS Code => Ctrl + Alt + N
console.log("Hello, World!");
// Using `let` for a variable that can be reassigned
let message = "This is a string.";
console.log(message); // Output: This is a string.

message = "This is a new string.";
console.log(message); // Output: This is a new string.

// Using `const` for a constant variable
const price = 10.99;
console.log(price); // Output: 10.99

// Using `var` (older keyword, generally less preferred)
var is_active = true;
console.log(is_active); // Output: true